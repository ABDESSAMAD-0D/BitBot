#include <unistd.h>
void two(char *str)
{
	int a = 0;
	while (str[a] != '\0')
	{
		if( a % 2 == 0)
		{
			write(1, &str[a], 1);
		}
		a++;
	}

}
int main(int ac, char **av)
{
	if (ac > 1)
	{
		two(av[1]);
	}
}
